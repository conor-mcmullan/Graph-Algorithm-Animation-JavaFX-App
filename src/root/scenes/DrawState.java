package root.scenes;

/**
 * The enum Draw state.
 */
public enum DrawState {
    /**
     * Add vertex draw state.
     */
    ADD_VERTEX,
    /**
     * Add edge draw state.
     */
    ADD_EDGE,
    /**
     * Remove object draw state.
     */
    REMOVE_OBJECT,
    /**
     * Move object draw state.
     */
    MOVE_OBJECT
}
