package root.scenes;

/**
 * The enum Animation step.
 */
public enum AnimationStep {
    /**
     * To start animation step.
     */
    TO_START,
    /**
     * To prev animation step.
     */
    TO_PREV,
    /**
     * To next animation step.
     */
    TO_NEXT,
    /**
     * To end animation step.
     */
    TO_END,
    /**
     * Autoplay animation step.
     */
    AUTOPLAY,
    /**
     * Begin animation step.
     */
    BEGIN,
    /**
     * Stop animation step.
     */
    STOP
}
